#' Star Trek color palettes
#'
#' Color palettes inspired by the colors used in _Star Trek_.
#'
#' @param palette Palette type.
#'   Currently there is one available option: `"uniform"`
#'   (7-color palette).
#' @param alpha Transparency level, a real number in (0, 1].
#'   See `alpha` in [grDevices::rgb()] for details.
#'
#' @export pal_startrek
#'
#' @importFrom grDevices col2rgb rgb
#' @importFrom scales manual_pal
#'
#' @author Nan Xiao | \email{me@nanx.me} | <https://nanx.me>
#'
#' @examples
#' library("scales")
#' show_col(pal_startrek("uniform")(7))
#' show_col(pal_startrek("uniform", alpha = 0.6)(7))
pal_startrek <- function(palette = c("uniform"), alpha = 1) {
  palette <- match.arg(palette)

  if (alpha > 1L || alpha <= 0L) stop("alpha must be in (0, 1]")

  raw_cols <- ggsci_db$"startrek"[[palette]]
  raw_cols_rgb <- col2rgb(raw_cols)
  alpha_cols <- rgb(
    raw_cols_rgb[1L, ], raw_cols_rgb[2L, ], raw_cols_rgb[3L, ],
    alpha = alpha * 255L, names = names(raw_cols),
    maxColorValue = 255L
  )

  manual_pal(unname(alpha_cols))
}

#' Star Trek color scales
#'
#' See [pal_startrek()] for details.
#'
#' @inheritParams pal_startrek
#' @param ... Additional parameters for [ggplot2::discrete_scale()].
#'
#' @export scale_color_startrek
#'
#' @importFrom ggplot2 discrete_scale
#'
#' @author Nan Xiao | \email{me@nanx.me} | <https://nanx.me>
#'
#' @rdname scale_startrek
#'
#' @examples
#' example_scatterplot() + scale_color_startrek()
#' example_barplot() + scale_fill_startrek()
scale_color_startrek <- function(palette = c("uniform"), alpha = 1, ...) {
  palette <- match.arg(palette)
  if (is_ggplot2_350()) {
    discrete_scale("colour", palette = pal_startrek(palette, alpha), ...)
  } else {
    discrete_scale("colour", scale_name = "startrek", palette = pal_startrek(palette, alpha), ...)
  }
}

#' @export scale_colour_startrek
#' @rdname scale_startrek
scale_colour_startrek <- scale_color_startrek

#' @export scale_fill_startrek
#' @importFrom ggplot2 discrete_scale
#' @rdname scale_startrek
scale_fill_startrek <- function(palette = c("uniform"), alpha = 1, ...) {
  palette <- match.arg(palette)
  if (is_ggplot2_350()) {
    discrete_scale("fill", palette = pal_startrek(palette, alpha), ...)
  } else {
    discrete_scale("fill", scale_name = "startrek", palette = pal_startrek(palette, alpha), ...)
  }
}
